package Reflection;

public class WithPrivateFinalField {

	private int a = 1;
	private final  String b = "Andrei";
	private String c= "Busea"; 
	private String d  = "Ina";
	String e = "Mia";
	
	
	
	
	public String toString() {
		return a + ", " + b + ", " + c + ", " + d + ", " + e;
	}
}
